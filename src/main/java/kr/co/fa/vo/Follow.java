package kr.co.fa.vo;

public class Follow {

	private User user;
	private int no;
	
	public Follow () {}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}
	
	
}
