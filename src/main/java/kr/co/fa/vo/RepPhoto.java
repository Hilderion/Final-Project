package kr.co.fa.vo;

public class RepPhoto {

	private int no;
	private String image;
	
	public RepPhoto () {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
}
